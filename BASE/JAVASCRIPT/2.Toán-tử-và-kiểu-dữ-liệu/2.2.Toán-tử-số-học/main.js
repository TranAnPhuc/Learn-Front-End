/*
        Toán tử số học
        +       --> Cộng
        -       --> Trừ
        *       --> Nhân
        **      --> Lũy thừa
        /       --> Chia
        %       --> Chia lấy số dư
        ++      --> Tăng 1 giá trị số
        --      --> Giảm 1 giá trị số
*/

var a = 5;
var b = 3;

// +
var cong = a + b;
console.log(cong);

// -
var tru = a - b;
console.log(tru);

// *
var nhan = a * b;
console.log(nhan);

// **
var luyThua = a ** b;
console.log(luyThua);

// /
var chia = a / b;
console.log(chia);

// %
var chiaLayDu = a % b;
console.log(chiaLayDu);

// ++
a++;
console.log(a);

// --
b--;
console.log(b);
