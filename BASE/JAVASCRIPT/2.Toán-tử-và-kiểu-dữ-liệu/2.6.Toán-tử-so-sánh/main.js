/*
Toán tử so sánh

Toán tử
==      ->      Bằng
!=      ->      Không bằng
>       ->      Lớn hơn
<       ->      Nhỏ hơn
>=      ->      Lớn hơn hoặc bằng
<=      ->      Nhỏ hơn hoặc bằng
*/

var a = 1;
var b = 2;

if (a == b) {
  console.log("1.Điều kiện đúng");
} else {
  console.log("1.Điều kiện sai");
}

if (a != b) {
  console.log("2.Điều kiện đúng");
} else {
  console.log("2.Điều kiện sai");
}

if (a > b) {
  console.log("3.Điều kiện đúng");
} else {
  console.log("3.Điều kiện sai");
}

if (a < b) {
  console.log("4.Điều kiện đúng");
} else {
  console.log("4.Điều kiện sai");
}

if (a >= b) {
  console.log("5.Điều kiện đúng");
} else {
  console.log("5.Điều kiện sai");
}

if (a <= b) {
  console.log("6.Điều kiện đúng");
} else {
  console.log("6.Điều kiện sai");
}
