// Câu lệnh rẽ nhánh - Switch

var date = 2;

switch (date) {
  case 2:
    console.log("Hôm nay là thứ 2");
    break;
  case 3:
    console.log("Hôm nay là thứ 3");
    break;
  case 4:
    console.log("Hôm nay là thứ 4");
    break;
  case 5:
    console.log("Hôm nay là thứ 5");
    break;
  case 6:
    console.log("Hôm nay là thứ 6");
    break;
  default:
    console.log("Không biết");
}

// 1. Khi nào cần so sánh tính đúng hay sai thì dùng if else
// 2. Khi nào biết trước giá trị thì dùng switch case với điều kiện
