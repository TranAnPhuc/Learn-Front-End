// Loop...
console.log("In ra dãy số 100 về 1");
for (var i = 100; i > 0; i--) {
  console.log(i);
}

console.log("In ra dãy số 0 đến 100 mỗi lần tăng lên 5");
for (var i = 0; i <= 100; i += 5) {
  console.log(i);
}

console.log("In ra dãy số từ 100 về 0 mỗi lần giảm về 5");
for (var i = 100; i >= 0; i -= 5) {
  console.log(i);
}
