// Promise (sync, async)
// Trong Javascript có 2 loại thực thi chính:
// Sync - Synchronous (Đồng bộ):
// - Mọi dòng lệnh được thực hiện theo thứ tự từ trên xuống dưới.
// - Mỗi dòng phải hoàn thành xong trước khi dòng tiếp theo được chạy.
// Async -  Asynchronous (Bất đồng bộ):
// - Một số dòng lệnh không cần chờ hoàn thành mà sẽ được xử lý sau, còn chương trình thì vẫn tiếp tục chạy tiếp.
// - Những việc này thường là: gọi API, đọc file, chờ thời gian (setTimeout,setInterval), fetch, XMLHttpRequest, v.v.
